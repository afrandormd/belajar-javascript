// for loop
for (let i = 1; i < 20; i++) {
    if (i % 2 !== 0) console.log(`${i} adalah bilangan ganjil`)
}

// convert ke while
let j = 20
while (j > 1) {
    if (j % 2 !== 0) console.log(`${j} adalah bilangan ganjil`)
j--
}

// do while loop
let x = 1
do {
    console.log('Hallo semuanya')
x++
} while (x < 10)